### UNPACKING ###

# Podemos extraer los elementos de una tupla en variables

tupla = ('Marcos', 'Eva', 'Marta', 'David')
(nombre1, nombre2, nombre3, nombre4)  = tupla

print (nombre1)
print (nombre2)
print (nombre3)
print (nombre4)

# Si el número de variantes no coincide con el número de elementos de la tupla usaremos un asterisco *

