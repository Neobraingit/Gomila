### LA FUNCIÓN PRINT ###

# La función print nos sirve para mostrar datos por pantalla

print ('Hello world') # No muestra el string al printear 

s1 = 'Esto es una variable ' # Printeamos una variable
print (s1)

s2 = str(1234355)
print (s2) # Printeamos un número entero

print (s1 + s2) # No podemos concatenar un string con un entero, por lo que debemos hacer el casteo del dato (podemos sustituir el + por comas)



