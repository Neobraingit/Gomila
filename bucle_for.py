### BUCLE FOR ###

s = 'Esto es un string'
for i in s :
    print (i)
    
# Función range() tiene 3 argumentos: start, stop, step

for i in range(0, 100, 2):
    print (i)
    
# El comando continue para la iteración actual y empieza la siguiente

for i in range(10, 0, -1):
    print (i)
    continue
print ('Continue')


