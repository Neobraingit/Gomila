### SUBSTRINGS ###

# Se puede acceder a un substrings con slicing

s = 'Soy fan del Heavy Metal'
print (s[0]) # Accedemos al primer caracter de la frase ya que empiezan a contar desde cero
print (s[0:4]) # Nos irá hasta el tres ya que el último índice nunca lo incluye
print (s[-1]) # Empezamos a contar por el final
print (s[::-1]) # Le damos la vuelta al string

